#Membership Operator:
# in - True if value is found in the sequence
# not in - True if value is not found in the sequence
nums = [4,14,20,8,9,5]

if 4 in nums:
    print("4 exists in List")

student = {
    "rollno" : 4,
    "name": "Omkar",
    "class": "TY"
}

if "age" not in student:
    print("Age is not exists in Student distionary")
